
function parseInput(input:string):string[][] {
    return input.split('\n').map(row => row.split(''));
}

interface Region {
    area: number;
    perimeter: number;
    corners: number;
    type: string;
}

interface Coord {
    x: number;
    y: number;
}

/**
 * Analyzes a 2D map to identify and characterize distinct regions using a flood-fill algorithm.
 * 
 * Algorithm Overview:
 * 1. Iterates through each cell in the 2D map
 * 2. For unvisited cells, performs a flood-fill to identify connected regions:
 *    - Uses a boundary list to track cells to explore
 *    - Only expands to adjacent cells of the same type
 *    - Marks cells as visited to avoid double-counting
 * 
 * For each region found, calculates:
 * - Area: Detected by counting how many locations we visit for this region
 * - Perimeter: Detected by counting how many adjacent cells are a different region
 * - Corners: Detected by checking 3-cell patterns like N,NE,E
 * 
 * @param map - 2D array of strings representing the map
 * @returns Array of Region objects containing area, perimeter, type, and corner counts
 */
function generateRegions(map:string[][]): Region[]{
    let regions:Region[] = [];
    let visited:Coord[] = [];

    for (let y = 0; y < map.length; y++) {
        for (let x = 0; x < map[y].length; x++) {

            // TODO: should split this into a separate function. It's easy to make the mistake of
            // using y or x instead of curr.y or curr.x, or similar. 
            let regionType:string = map[y][x];
            let regionArea = 0;
            let regionPerimeter = 0;
            let regionCorners = 0; 

            let boundary:Coord[] = [{x, y}];
            
            while(boundary.length > 0) {

                let curr = boundary.pop();

                // if we haven't visited this location
                if (curr && !visited.some(loc => (loc.x === curr.x && loc.y === curr.y))) {
                    regionArea++; // first ever visit of this location, increment the region's area

                    visited.push(curr);

                    let candidates:Coord[] = [
                        {x: curr.x+1, y:curr.y},
                        {x: curr.x, y:curr.y+1},
                        {x: curr.x-1, y:curr.y},
                        {x: curr.x, y:curr.y-1}
                    ];

                    let validCandidates = candidates.filter(candidate => matchesType(map, candidate.x, candidate.y, regionType));

                    // add valid candidates to perimeter
                    boundary.push(...validCandidates);

                    // any invalid candidate must be a perimeter
                    regionPerimeter += candidates.length - validCandidates.length;

                    let N =  matchesType(map, curr.x,   curr.y-1, regionType);
                    let NE = matchesType(map, curr.x+1, curr.y-1, regionType);
                    let E =  matchesType(map, curr.x+1, curr.y,   regionType);
                    let SE = matchesType(map, curr.x+1, curr.y+1, regionType);
                    let S =  matchesType(map, curr.x,   curr.y+1, regionType);
                    let SW = matchesType(map, curr.x-1, curr.y+1, regionType);
                    let W =  matchesType(map, curr.x-1, curr.y,   regionType);
                    let NW = matchesType(map, curr.x-1, curr.y-1, regionType);

                    if (isCorner(N, NE, E)) regionCorners++;
                    if (isCorner(E, SE, S)) regionCorners++;
                    if (isCorner(S, SW, W)) regionCorners++;
                    if (isCorner(W, NW, N)) regionCorners++;

                }
            }

            if (regionArea > 0 && regionPerimeter > 0) {
                //console.log(`x ${x} y ${y} regionArea ${regionArea} regionPerimeter ${regionPerimeter} regionType ${regionType} regionCorners ${regionCorners}`);
                regions.push({area:regionArea, perimeter:regionPerimeter, type:regionType, corners:regionCorners});
            }
        }
    }

    return regions;
}

function matchesType(map:string[][], x:number, y:number, type:string): boolean {
    return y >= 0 && x >= 0 && y < map.length && x < map[y].length && map[y][x] === type;
}

function isCorner(side1:boolean, corner:boolean, side2:boolean):boolean {
    return isExternalCorner(side1, corner, side2) || isInternalCorner(side1, corner, side2);
}

//  ..  .#    // note diagonals like this are two separate corners that happen to be of same type, so we don't care about corner's value
//  #.  #.
function isExternalCorner(side1:boolean, corner:boolean, side2:boolean):boolean {
    //console.log(`external ${!side1 && !corner && !side2}`);
    return !side1 && !side2;
}

//  #.
//  ##
function isInternalCorner(side1:boolean, corner:boolean, side2:boolean):boolean {
    //console.log(`internal ${side1 && !corner && side2}`);
    return side1 && !corner && side2;
}

function calculatePricePart1(regions:Region[]): number {
    return regions.reduce((accumulator, region) => (accumulator + region.area*region.perimeter), 0);
}

// num corners == num sides
function calculatePricePart2(regions:Region[]): number {
    return regions.reduce((accumulator, region) => (accumulator + region.area*region.corners), 0);
}

